#ifdef HAVE_CONFIG_H
#include "config.h"
#endif

#include <stdio.h>
#include <stdarg.h>
#include <stdlib.h>
#include <string.h>

#include <gsasl.h>

int
main (void)
{
  const char *this = NULL, *last = NULL;
  const char *name;
  size_t i = 0;

  printf ("@c This file is automatically generated.  DO NOT EDIT!\n");
  printf ("@c Instead, edit print-errors.c and re-run.\n");

  do
    {
      last = this;

      this = gsasl_strerror (i);
      name = gsasl_strerror_name (i);

      if (name != NULL)
	printf ("\n@item @code{%s}\n%s\n", name, this);

      if (this == NULL)
	return EXIT_FAILURE;

      i++;
    }
  while (this != last);

  return EXIT_SUCCESS;
}
